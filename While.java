/* Método principal da classe */
public class While {
    public static void main(String[] args) { // Fluxo de repetição usando While
        int i = 0; // a variável i recebe o valor zero (0)

        while (i < 10) { // enquanto i for menor que 10
            System.out.println(i); // imprima valor de i
            i++; // incremento da variável i
        } // fim do método
    }// fim do método
} // fim da classe